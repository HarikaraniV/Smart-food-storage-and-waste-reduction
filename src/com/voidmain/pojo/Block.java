package com.voidmain.pojo;

public class Block {

	private int blockId;
	private String wharehouse;
	private String catpacity;
	private String isHavingColdStorage;
	private String description;
	
	public int getBlockId() {
		return blockId;
	}
	public void setBlockId(int blockId) {
		this.blockId = blockId;
	}
	public String getWharehouse() {
		return wharehouse;
	}
	public void setWharehouse(String wharehouse) {
		this.wharehouse = wharehouse;
	}
	public String getCatpacity() {
		return catpacity;
	}
	public void setCatpacity(String catpacity) {
		this.catpacity = catpacity;
	}
	public String getIsHavingColdStorage() {
		return isHavingColdStorage;
	}
	public void setIsHavingColdStorage(String isHavingColdStorage) {
		this.isHavingColdStorage = isHavingColdStorage;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
}
